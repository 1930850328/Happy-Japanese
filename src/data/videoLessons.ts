function createGradientCover(title: string, subtitle: string, accentA: string, accentB: string) {
  const svg = `
    <svg xmlns="http://www.w3.org/2000/svg" width="720" height="960" viewBox="0 0 720 960">
      <defs>
        <linearGradient id="bg" x1="0%" y1="0%" x2="100%" y2="100%">
          <stop offset="0%" stop-color="${accentA}" />
          <stop offset="100%" stop-color="${accentB}" />
        </linearGradient>
      </defs>
      <rect width="720" height="960" rx="48" fill="url(#bg)" />
      <circle cx="118" cy="132" r="72" fill="rgba(255,255,255,0.35)" />
      <circle cx="594" cy="812" r="118" fill="rgba(255,255,255,0.26)" />
      <circle cx="610" cy="142" r="46" fill="rgba(255,255,255,0.22)" />
      <text x="58" y="130" fill="#7d5647" font-size="26" font-family="sans-serif">${subtitle}</text>
      <text x="58" y="288" fill="#4a352d" font-size="56" font-family="sans-serif">${title}</text>
      <text x="58" y="352" fill="#5f4b43" font-size="26" font-family="sans-serif">Official Clip Micro Lesson</text>
    </svg>
  `
  return `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`
}

function officialCover(title: string, series: string, accentA: string, accentB: string) {
  return createGradientCover(title, `${series} / 官方公开 PV 微课`, accentA, accentB)
}

